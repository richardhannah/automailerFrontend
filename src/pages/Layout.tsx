interface SocialLink {
  platform: string;
  url: string;
}

export default function Layout() {
  const { user, logout } = useAuth();
  const navigate = useNavigate();
  const [showSignIn, setShowSignIn] = useState(false);
  const [socialLinks, setSocialLinks] = useState<SocialLink[]>([]);

  useEffect(() => {
    fetch(`${API_URL}/api/SocialMediaLinks`)
      .then(res => res.ok ? res.json() : [])
      .then((data: SocialLink[]) => setSocialLinks(data.filter(l => l.url)));
  }, []);

  const handleLogout = () => {
    logout();
    navigate('/');
  };

  return (
    <div className="layout">
      <header className="header">
        <Link to="/" className="logo"><img src="/tellyboxLogo.png" alt="TellyBox" className="logo-img" /></Link>
        <nav>
          {user ? (
            <>
              <Link to="/packages">Packages</Link>
              {user.role === 'Admin' && (
                <>
                  <Link to="/admin">Users</Link>
                  <Link to="/customers">Customers</Link>
                  <Link to="/templates">Templates</Link>
                  <Link to="/enquiries">Enquiries</Link>
                  <Link to="/subscriptions">Subscriptions</Link>
                  <Link to="/settings">Settings</Link>
                </>
              )}
              <Link to="/my-account">My Account</Link>
              <span className="user-info">{user.username} ({user.role})</span>
              <button onClick={handleLogout} className="btn-logout">Logout</button>
              <LiveChat />
              {socialLinks.length > 0 && (
                <div className="header-social">
                  {socialLinks.map(l => {
                    const icon = SOCIAL_ICONS[l.platform];
                    if (!icon) return null;
                    return (
                      <a key={l.platform} href={l.url} target="_blank" rel="noopener noreferrer" className="social-icon-link" title={l.platform.charAt(0).toUpperCase() + l.platform.slice(1)}>
                        <svg viewBox={icon.viewBox} fill="currentColor" width="18" height="18"><path d={icon.path} /></svg>
                      </a>
                    );
                  })}
                </div>
              )}
            </>
          ) : (
            <>
              {socialLinks.length > 0 && (
                <div className="header-social">
                  {socialLinks.map(l => {
                    const icon = SOCIAL_ICONS[l.platform];
                    if (!icon) return null;
                    return (
                      <a key={l.platform} href={l.url} target="_blank" rel="noopener noreferrer" className="social-icon-link" title={l.platform.charAt(0).toUpperCase() + l.platform.slice(1)}>
                        <svg viewBox={icon.viewBox} fill="currentColor" width="18" height="18"><path d={icon.path} /></svg>
                      </a>
                    );
                  })}
                </div>
              )}
              <LiveChat />
              <button onClick={() => setShowSignIn(true)} className="btn-signin">Sign In</button>
            </>
          )}
        </nav>
      </header>
      <main className="main">
        <Outlet />
      </main>
      <footer className="footer">
        {socialLinks.length > 0 && (
          <div className="footer-social">
            {socialLinks.map(l => {
              const icon = SOCIAL_ICONS[l.platform];
              if (!icon) return null;
              return (
                <a
                  key={l.platform}
                  href={l.url}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="social-icon-link"
                  title={l.platform.charAt(0).toUpperCase() + l.platform.slice(1)}
                >
                  <svg viewBox={icon.viewBox} fill="currentColor" width="20" height="20">
                    <path d={icon.path} />
                  </svg>
                </a>
              );
            })}
          </div>
        )}
        <p className="footer-text">&copy; {new Date().getFullYear()} TellyBox</p>
      </footer>
      {showSignIn && <SignInModal onClose={() => setShowSignIn(false)} />}
    </div>
  );
}
